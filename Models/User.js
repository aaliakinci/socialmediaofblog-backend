const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const UserSchema = new Schema({
	name:{
		type:String,
		required:true,
	},
	surname:{
		type:String,
		required:true,
	},
	username:{
		type:String,
		required:true,
		unique:true,
	},
	phoneNumber:{
		type:String,
		required:true,
		unique:true,
	},
	email:{
		type:String,
		unique:true,
		required:true
	},
	password:{
		type:String,
		required:true,
		minlength:8,
	},
	gender:{
		type:String,
		required:true
	},
	profilPicture:{
		type:String,
	},
	birthDate:{
		type:Date,
	},
	createAt:{
		type:Date,
		default:Date.now
	},
	description:{
		type:String,
		maxlength:40
	},
	isAdmin:{
		type:Boolean,
		default:false
	},
	isEditor:{
		type:Boolean,
		default:false
	},
	follows:{
		type:mongoose.Types.ObjectId,
		ref:'users'
	},
	followers:{
		type:mongoose.Types.ObjectId,
		ref:'users'
	},
	articles:{
		type:mongoose.Types.ObjectId,
		ref:'articles'
	},
	comments:{
		type:mongoose.Types.ObjectId,
		ref:'comments'
	},
	likes:{
		type:mongoose.Types.ObjectId,
		ref:'likes'
	}
});
module.exports=mongoose.model('user',UserSchema)